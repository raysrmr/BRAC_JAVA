public class TriDemo
    {
            public static void main(String as[])
                {                     
                          for(int i=1;i<=5;i++)
                                 {
                                       for(int j=1;j<=i;j++)
                                            System.out.print(j+"\t");   
                                       System.out.println("\n");   
                                 }           
                }
    }