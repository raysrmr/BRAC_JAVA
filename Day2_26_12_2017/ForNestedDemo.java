public class ForNestedDemo
    {
            public static void main(String as[])
                {                      
                      for(int i=0;i<3;i++)
                         {
                              for(int j=0;j<3;j++)
                                 {
                                         System.out.println("IN Brac: i="+i+"    j="+j);
                                 }
                         }
                }
    }